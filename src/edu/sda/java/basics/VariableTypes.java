package edu.sda.java.basics;

public class VariableTypes {
    private byte byteValue = 3; //default is 0
    private short shortValue = 8; //default is 0
    private int intValue = 122; //default is 0
    private long longValue = 122L; //default is 0L

    private float floatValue = 1.22f; //default is 0.0f

    private double doubleValue = 1.22d; //default is 0.0d

    private boolean booleanVal; //this is FALSE by default - if we do not set a value to variable, it is initialised with default one
    private boolean booleanTrue = true;
    private boolean booleanFalse = false;

    private char oneLetter = 'a'; //character in single quotes
    private char oneSpecialSign = '\n'; //new line sign
    private char oneTabSign = '\t'; //tab sign

    private String oneString = "A string"; // String - set of signs - in double quotes
}
